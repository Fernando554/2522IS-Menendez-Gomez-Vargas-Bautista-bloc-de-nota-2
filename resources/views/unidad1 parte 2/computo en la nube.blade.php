      <h1>COMPUTO DE LA NUBE</h1>
      <h2>¿Que es la computacion en la nube?</h2>
      <p> La computación en la nube es la entrega de recursos informáticos, incluidos el almacenamiento, la potencia de procesamiento, las bases de datos, las redes, los análisis, la inteligencia artificial y las aplicaciones de software, a través de Internet (la nube). </p>
      <h2>CARACTERISTICAS</h2>
      <p>Antes de la llegada de la computación en la nube, las organizaciones compraban y mantenían una infraestructura local de TI. Aunque el ahorro de costes impulsó gran parte del cambio inicial a la nube, muchas organizaciones opinan que las infraestructuras de nube pública, privada o híbrida ofrecen numerosas ventajas.</p>
      <h3>FUNDAMENTOS</h3>
      <p>Cuando una empresa decide "migrar a la nube", significa que traslada su infraestructura de TI fuera del entorno local, a un centro de datos mantenido por un proveedor de Cloud Computing (como Oracle). Un proveedor de nube líder de la industria tiene la responsabilidad de gestionar la infraestructura de TI del cliente, integrar las aplicaciones y desarrollar nuevas capacidades y funcionalidades para seguir el ritmo de las demandas del mercado.</p>
      <h3>TIPOS DE COMPUTOS DE NUBES</h3>
      <p>NUBE PRIVADA</p>
      <p>La nube privada es de uso exclusivo de una organización. Se puede alojar en la ubicación de la organización o en el centro de datos del proveedor de la nube. La nube privada proporciona un nivel más alto de seguridad y control.</p>
      <p>NUBE HIBRIDA</p>
      <p>Como su nombre indica, una nube híbrida es una combinación de nubes públicas y privadas. En general, el cliente aloja sus aplicaciones críticas en sus propios servidores para mayor seguridad y control, y las aplicaciones secundarias se almacenan en el entorno de un proveedor de nube.</p>
      <p>MULTINUBE</p>
      <p>La principal diferencia entre nube híbrida y multinube es el uso de varios dispositivos de Cloud Computing y almacenamiento en una sola arquitectura. Hay que centrarse en añadir algunas palabras clave en el cuerpo.</p>
      
