<h1>SISTEMAS DE COMPUTADORAS CENTRALES Y DEDICADAS</h1>
      <img class="imgr borderedbox inspace-5" src="../images/demo/images.jpg" alt="">
      <p>Computadora central. También conocida como mainframe, es una computadora grande, potente y costosa, usada principalmente por una gran compañía para el procesamiento de una gran cantidad de datos; por ejemplo, para el procesamiento de transacciones bancarias.</p>
      <p>La capacidad de una computadora central se define tanto por la velocidad de su CPU como por su gran memoria interna, su alta y gran capacidad de almacenamiento externo, sus resultados en los dispositivo E/S rápidos y considerables, la alta calidad de su ingeniería interna que tiene como consecuencia una alta fiabilidad y soporte técnico caro pero de alta calidad. Una computadora central puede funcionar durante años sin problemas ni interrupciones y las reparaciones del mismo pueden ser realizadas mientras está funcionando. </p>
      <img class="imgl borderedbox inspace-5" src="../images/demo/compu-centrales.png" alt="">
      <p>A menudo, las computadoras centrales soportan miles de usuarios de manera simultánea que se conectan mediante falsos terminales. Algunas computadoras centrales pueden ejecutar o dar cobijo a muchos sistemas operativos y por lo tanto, no funcionan como una computadora sola, sino como varias computadoras virtuales. En este papel, una computadora central por sí sola puede remplazar docenas o cientos de pequeñas computadoras personales, reduciendo los costes administrativos y de gestión al tiempo que ofrece una escalabilidad y fiabilidad mucho mejor. La fiabilidad se consigue por la independencia de sus componentes internos señalada anteriormente, y la escalabilidad se logra porque los recursos físicos de la computadora pueden ser redistribuidos entre los terminales virtuales según las necesidades; esto es mucho más difícil de hacer con las computadoras personales, porque para quitar o añadir nuevos componentes físicos hay que desenchufar la computadora muchas veces y las limitaciones de dichos componentes son mucho mayores.</p>
      
      
      <p>Cuando una computadora central actúa como el centro de operaciones de muchos terminales virtuales, puede ofrecer la potencia necesaria para que dichas computadoras operen de manera eficiente, pero también la flexibilidad de las redes de computadoras personales.</p>
      
      <b>Comparación con las supercomputadoras</b>
     <p>La distinción entre supercomputadores y computadoras centrales no es muy sencilla, pero generalmente se puede decir que las supercomputadoras se centran en los problemas limitados por la velocidad de cálculo mientras que las computadoras centrales se centran en problemas limitados por los dispositivos de E/S y la fiabilidad. En consecuencia:</p>
     
     <p>Las supercomputadoras suelen explotar paralelismos masivos, a menudo con miles de procesadores, mientras que las computadoras centrales tienen un solo o un pequeño número de procesadores (como mucho varias docenas).</p>

     <p>Debido al paralelismo visible al programador, las supercomputadoras son muy complicadas de programar; en las computadoras centrales, el limitado paralelismo (si existe) está normalmente escondido del programador.</p>

     <p>Las supercomputadoras son optimizadas para cálculos complicados que tienen lugar sobre todo en la memoria, mientras que las computadoras centrales son optimizadas para cálculos simples que implican grandes cantidades de datos externos a los que se accede desde bases de datos.</p>

     <p>Las supercomputadoras suelen dedicarse a la ciencia y al ejército, mientras que las computadoras centrales suelen dedicarse a las empresas y las aplicaciones administrativas del gobierno.</p>

     <b>CARACTERISTICAS</b>

     <p>Vel. de proceso: Cientos de millones de instrucciones por segundo o mas.</p>
      
     <p>Usuario a la vez: Centenares o miles.</p>

     <p>Tamaño: Requieren instalaciones especiales y aire acondicionado.</p>

     <p>Facilidad de uso: Para especialistas.</p>

     <p>Clientes usuales: Grandes corporaciones y gobiernos.</p>

     <p>Penetración social: Baja.</p>

     <p>Impacto social: Muy alto, aunque pasa inadvertido, la sociedad industrial moderna no puede funcionar sin ellas.</p>

     <p>Parque instalado: Miles en todo el mundo.</p>

     <p>Costo: Centenares de miles de dólares o más.</p>

