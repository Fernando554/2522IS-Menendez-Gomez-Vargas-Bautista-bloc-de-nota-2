      <h1>EVOLUCION DE LOS SISTEMAS DE INFORMACION</h1>
      <img class="imgr borderedbox inspace-5" src="../images/demo/evolucion-sistemas.png" alt="">
      <p>La evolución de los sistemas de información expone el desarrollo de estos métodos que cumplen la tarea de organizar y administrar recursos, motivo por el que muchos dicen que son tan antiguos como la civilización misma.

        El origen de estos sistemas lo podemos encontrar en Egipto aproximadamente en el 4.000 a.C. donde se realizó un censo de población, censo que se utilizó la recoger información y utilizarla para la toma de decisiones.
        
        Sin embargo, desde entonces los sistemas de información han ido evolucionando, y ya no se limitan solo a lo físico, sino que también forma una parte crucial de la informática.
        
        Por ello, en esta ocasión te vamos a hablar de la evolución de los sistemas de información en línea del tiempo. No obstante, antes es propicio que entiendas mejor qué son exactamente estos sistemas de información, por qué son tan importantes, como se caracterizan y como están constituidos.</p>
      
        <b> EVOLUCION</b>


        <div><b> 1950-1960</b> </div>
      
        <p>Durante este período, el papel de SI fue principalmente para realizar actividades como el procesamiento de transacciones, el mantenimiento de registros y la contabilidad. SI se utilizó principalmente para el procesamiento electrónico de datos.

        EDP ​​se describe como el uso de computadoras para registrar, clasificar, manipular y resumir datos. También se llama procesamiento de información o procesamiento de datos automático.
        
        Sistema de procesamiento de transacciones (TPS) fue el primer sistema computarizado desarrollado para procesar datos comerciales. TPS estaba principalmente dirigido al personal administrativo de una organización. El TPS inicial utilizaba datos de procesamiento por lotes que se acumularon durante un período y todas las transacciones se procesaron posteriormente.</p>
      <img class="imgl borderedbox inspace-5" src="../images/demo/3-2.png" alt="">

      <b> 1960-1970</b>
      <p>Durante esta era, el rol de SI evolucionó desde TPS hasta Management Information Systems (MSI) o Sistemas de información gerenciales. MSI procesa los datos en informes informativos útiles y proporciona a los gerentes las herramientas para organizar evaluar y administrar de manera eficiente los departamentos dentro de una organización. MSI entrega información en forma de pantallas e informes preespecificados para respaldar la toma de decisiones empresariales. Algunos ejemplos de resultados de MSI son los sistemas de informes de tendencias de costos, análisis de ventas y rendimiento de producción.

        Por lo general, MSI genera tres tipos básicos de información que son:
        
        Los informes detallados de información típicamente confirman las actividades de procesamiento de transacciones. Un informe de pedido detallado es un ejemplo de informe detallado.</p>
      
      <b>1970-1980</b>
      <p>En esta era, un avance importante fue la introducción de las computadoras personales (PC). Con la introducción de las PC, se produjo la distribución de la potencia informática o de procesamiento en toda la organización. La función de SI está fuertemente asociada con la administración en lugar de un enfoque técnico en una organización. El papel se centró en el "sistema interactivo basado en computadoras" para ayudar a los responsables de la toma de decisiones a resolver problemas.

        Este nuevo rol de los sistemas de información para proporcionar soporte ad-hoc interactivo para el proceso de toma de decisiones a los gerentes y otros profesionales de negocios se denomina Sistemas de Soporte de Decisiones (DSS). DSS sirve el nivel de planificación, gestión y operaciones de una organización, por lo general, la alta dirección.</p>
      
      <b>1980-1990</b>
      <p>Este período dio lugar a la informática departamental debido a que muchas organizaciones compran su propio hardware y software para satisfacer sus necesidades departamentales. En lugar de esperar el apoyo indirecto del departamento de servicio corporativo centralizado, los empleados podrían usar sus propios recursos para respaldar sus requisitos laborales. Esta tendencia dio lugar a nuevos desafíos de incompatibilidad de datos, integridad y conectividad en diferentes departamentos. Además, los altos ejecutivos no usaban DSS ni MSI, por lo que se desarrollaron sistemas de información ejecutivo (ESI) o sistemas de soporte ejecutivo (ESS).</p>
      
      <b>1990-2000</b>

      <p>Durante esta era, el rápido crecimiento de las intranets, extranets, internet y otras redes globales interconectadas cambiaron drásticamente las capacidades de SI en los negocios. Se hizo posible circular el conocimiento a diferentes partes del mundo independientemente del tiempo y el espacio.

        Este período también vio una aparición de sistemas de planificación de recursos empresariales (ERP). ERP es una forma específica de organización de un sistema de información estratégica que incorpora todos los componentes de una organización, incluidas la fabricación, las ventas, la gestión de recursos, la planificación de recursos humanos y el marketing.
        
        Además, hubo un avance en el desarrollo y la aplicación de técnicas de inteligencia artificial (IA) a los sistemas de información comercial. Sistemas expertos (ES) y sistemas de gestión del conocimiento (KMS) interconectados entre sí.</p>
      
        <b>2000</b>

        <p>Internet y las tecnologías y aplicaciones relacionadas cambiaron la forma en que operan las empresas y las personas trabajan. Las funciones de los sistemas de información en este período siguen siendo las mismas que hace 50 años: mantenimiento de registros, gestión de informes, procesamiento de transacciones, gestión de soporte y procesos de gestión de la organización. Se usa para respaldar el proceso comercial, la toma de decisiones y las ventajas competitiva.</p>
